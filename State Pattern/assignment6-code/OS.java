

public interface OS {
	public String toString();
}

class Linux implements OS {
	public String toString() {
		return "Linux OS";
	}
}

class Symbian implements OS {
	public String toString() {
		return "Symbian OS";
	}
}

class Android implements OS {
	public String toString() {
		return "Android OS";
	}
}

class WindowsME implements OS {
	public String toString() {
		return "Windows-ME OS";
	}
}

class WindowsXP implements OS {
	public String toString() {
		return "Windows-XP OS";
	}
}